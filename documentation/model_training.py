"""## MomentumWindowEncoder"""

class MomentumWindowEncoder:
    """
    EMA shadow copy of WindowEncoder used as the NWP prediction target.

    Constructed once at the start of Stage 1 by deep-copying the online
    WindowEncoder.  Parameters are never updated by gradients — only by
    the EMA rule:
        θ_target = decay * θ_target + (1 - decay) * θ_online

    Usage
    -----
    momentum_enc = MomentumWindowEncoder(model.window_encoder, decay=EMA_DECAY)

    # Inside training step (after optimizer.step):
    momentum_enc.update(model.window_encoder)

    # To get target embeddings:
    with torch.no_grad():
        target_embeds = encode_all_windows(momentum_enc.encoder, ...)
    """

    def __init__(
        self,
        online_encoder : WindowEncoder,
        decay          : float = 0.99,
    ) -> None:
        self.decay   = decay
        self.encoder = copy.deepcopy(online_encoder)

        # Target encoder never receives gradients
        for p in self.encoder.parameters():
            p.requires_grad_(False)

        self.encoder.eval()

    @torch.no_grad()
    def update(self, online_encoder: WindowEncoder) -> None:
        for p_target, p_online in zip(
            self.encoder.parameters(),
            online_encoder.parameters(),
        ):
            p_target.data.mul_(self.decay).add_(
                p_online.data, alpha=1.0 - self.decay
            )

    def to(self, device):
        self.encoder = self.encoder.to(device)
        return self


