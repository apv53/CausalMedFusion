class CXRTokenBuilder(nn.Module):
    """
    Input:
        cxr           [B, n_cxrs, D_CXR]
        cxr_view_time [B, n_cxrs, 3]   [view_pos, t_global, t_window]
                      (col 0 mask has already been stripped off)
    Output:
        cxr_tokens    [B, n_cxrs, d_model]
    """

    def __init__(
        self,
        global_time_encoder : nn.Module,
        window_time_encoder : nn.Module,
        modality_encoder    : nn.Embedding,
        modality_id         : int,
        d_model             : int,
        d_embed             : int,
    ) -> None:
        super().__init__()

        self.global_time_encoder = global_time_encoder
        self.window_time_encoder = window_time_encoder
        self.modality_encoder    = modality_encoder

        self.register_buffer('mod_id', torch.tensor(modality_id, dtype=torch.long))
        self.d_model = d_model

        self.cxr_encoder  = CXREmbeddingEncoder(d_model, d_embed)
        self.token_fusion = nn.Linear(d_model * 2, d_model)
        self.token_norm   = nn.LayerNorm(d_model)

    def forward(
        self,
        cxr           : torch.Tensor,   # [B, n_cxrs, D_CXR]
        cxr_view_time : torch.Tensor,   # [B, n_cxrs, 3]  [view_pos, t_global, t_window]
    ) -> torch.Tensor:                  # [B, n_cxrs, d_model]

        B, n_cxrs, _ = cxr.shape

        view_pos = cxr_view_time[:, :, 0]
        t_global = cxr_view_time[:, :, 1]
        t_window = cxr_view_time[:, :, 2]

        cxr_emb      = self.cxr_encoder(cxr, view_pos)
        t_global_emb = self.global_time_encoder(t_global)
        t_window_emb = self.window_time_encoder(t_window)
        mod_emb      = self.modality_encoder(self.mod_id.expand(B, n_cxrs))

        context = mod_emb + t_global_emb + t_window_emb
        token   = self.token_fusion(torch.cat([cxr_emb, context], dim=-1))
        return self.token_norm(token)


