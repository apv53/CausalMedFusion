def build_causal_block_mask(
    n_positions : int,
    K           : int,
    device      : torch.device,
) -> torch.Tensor:
    """
    Returns bool tensor [n_positions*K, n_positions].
    True  = masked (ignore this key).
    False = attend to this key.
    """
    n_queries = n_positions * K
    mask = torch.ones(n_queries, n_positions, dtype=torch.bool, device=device)

    for p in range(n_positions):
        row_start = p * K
        row_end   = row_start + K
        # Queries in block p can attend to windows 0..p inclusive
        mask[row_start:row_end, : p + 1] = False

    return mask   # [n_queries, n_windows]


