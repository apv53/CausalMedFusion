"""## COLLATE FN"""

def collate_fn(batch: List[dict]) -> dict:
    """
    Pad and stack a list of per-stay dicts into batch tensors.

    Tensor shapes returned  (B = batch size, W = MAX_WINDOWS):

        cxr              [B, W, MAX_CXRS,    D_CXR]
        cxr_meta         [B, W, MAX_CXRS,    4]
        report           [B, W, MAX_REPORTS, D_REPORT]
        report_meta      [B, W, MAX_REPORTS, 4]
        lab              [B, W, MAX_LABS,    5]
        vital            [B, W, MAX_VITALS,  8]
        interv_t         [B, W, 5]
        interv_t1        [B, W, 5]
        interv_t1_valid  [B, W]   bool
        mortality        [B, W]
        window_mask      [B, W]   float  1=real window, 0=padded
    """
    B = len(batch)

    cxr             = torch.zeros(B, MAX_WINDOWS, MAX_CXRS,    D_CXR)
    cxr_meta        = torch.zeros(B, MAX_WINDOWS, MAX_CXRS,    4)
    report          = torch.zeros(B, MAX_WINDOWS, MAX_REPORTS, D_REPORT)
    report_meta     = torch.zeros(B, MAX_WINDOWS, MAX_REPORTS, 4)
    lab             = torch.zeros(B, MAX_WINDOWS, MAX_LABS,    5)
    vital           = torch.zeros(B, MAX_WINDOWS, MAX_VITALS,  8)
    interv_t        = torch.zeros(B, MAX_WINDOWS, 5)
    interv_t1       = torch.zeros(B, MAX_WINDOWS, 5)
    interv_t1_valid = torch.zeros(B, MAX_WINDOWS, dtype=torch.bool)
    mortality       = torch.zeros(B, MAX_WINDOWS)
    window_mask     = torch.zeros(B, MAX_WINDOWS)

    for i, stay in enumerate(batch):
        n_windows = min(len(stay['cxr']), MAX_WINDOWS)
        window_mask[i, :n_windows] = 1.0

        for w in range(n_windows):
            cxr[i, w]          = pad_tensor(stay['cxr'][w],         MAX_CXRS)
            cxr_meta[i, w]     = pad_tensor(stay['cxr_meta'][w],    MAX_CXRS)
            report[i, w]       = pad_tensor(stay['report'][w],       MAX_REPORTS)
            report_meta[i, w]  = pad_tensor(stay['report_meta'][w],  MAX_REPORTS)
            lab[i, w]          = pad_tensor(stay['lab'][w],          MAX_LABS)
            vital[i, w]        = pad_tensor(stay['vital'][w],        MAX_VITALS)

            interv_t[i, w]        = stay['interventions_t'][w]
            interv_t1[i, w]       = stay['interventions_t1'][w]
            interv_t1_valid[i, w] = stay['interv_t1_valid'][w]
            mortality[i, w]       = stay['mortality'][w]

    return {
        'cxr'             : cxr,
        'cxr_meta'        : cxr_meta,
        'report'          : report,
        'report_meta'     : report_meta,
        'lab'             : lab,
        'vital'           : vital,
        'interv_t'        : interv_t,
        'interv_t1'       : interv_t1,
        'interv_t1_valid' : interv_t1_valid,
        'mortality'       : mortality,
        'window_mask'     : window_mask,
    }


