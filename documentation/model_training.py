def stage2_loss(model: ICUModel, batch: dict) -> tuple:
    """
    BCE mortality loss with pos_weight, stay-length normalised.
    """
    cxr         = batch['cxr'].to(device)
    cxr_meta    = batch['cxr_meta'].to(device)
    report      = batch['report'].to(device)
    report_meta = batch['report_meta'].to(device)
    lab         = batch['lab'].to(device)
    vital       = batch['vital'].to(device)
    window_mask = batch['window_mask'].to(device)
    mortality   = batch['mortality'].to(device)

    severity, _ = model.get_severity(
        cxr, cxr_meta, report, report_meta, lab, vital, window_mask,
    )

    mort_logits = model.mortality_head(severity).squeeze(-1)   # [B, W]

    bce = F.binary_cross_entropy_with_logits(
        mort_logits,
        mortality,
        pos_weight = torch.tensor(S2_MORT_POS_WEIGHT, device=device),
        reduction  = 'none',
    )                                                           # [B, W]

    bce       = bce * window_mask
    per_stay  = bce.sum(dim=1) / window_mask.sum(dim=1).clamp(min=1)
    total     = per_stay.mean()

    return total, {'total': total.item()}


