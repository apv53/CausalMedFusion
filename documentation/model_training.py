def f1_optimal_threshold(labels, probs, n_thresholds=99):
    """Find threshold that maximises F1 on binary labels."""
    thresholds  = np.linspace(0.01, 0.99, n_thresholds)
    best_f1     = 0.0
    best_thresh = 0.5
    for t in thresholds:
        pred = (probs >= t).astype(int)
        tp = ((pred == 1) & (labels == 1)).sum()
        fp = ((pred == 1) & (labels == 0)).sum()
        fn = ((pred == 0) & (labels == 1)).sum()
        prec = tp / (tp + fp + 1e-8)
        rec  = tp / (tp + fn + 1e-8)
        f1   = 2 * prec * rec / (prec + rec + 1e-8)
        if f1 > best_f1:
            best_f1     = float(f1)
            best_thresh = float(t)
    return best_thresh, best_f1


