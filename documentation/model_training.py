"""## ICU STAY DATASET"""

class ICUStayDataset(Dataset):
    """
    PyTorch Dataset indexed by ICU stay.

    One item = all windows of one stay, returned as a dict of lists
    (one element per window).  collate_fn pads and stacks these into
    batch tensors.

    Parameters
    ----------
    parquet_path  : path to train_set.parquet or val_set.parquet
    cxr_store     : EmbeddingStore for CXR image embeddings
    report_store  : EmbeddingStore for report text embeddings
    """

    def __init__(
        self,
        parquet_path : str,
        cxr_store    : EmbeddingStore,
        report_store : EmbeddingStore,
    ) -> None:

        df = pd.read_parquet(str(parquet_path))

        # Parse JSON columns
        for col in ('cxrs', 'reports', 'labs', 'vitals',
                    'interventions_t', 'interventions_t1'):
            df[col] = df[col].apply(
                lambda x: json.loads(x) if isinstance(x, str) else x
            )

        # Always process windows in order 1 → 6
        df = df.sort_values(['stay_id', 'window_id']).reset_index(drop=True)

        self._groups   = df.groupby('stay_id', sort=False)
        self.stay_ids  = list(self._groups.groups.keys())

        # Max mortality label per stay — used by weighted sampler
        self.stay_mortality = (
            df.groupby('stay_id')['mortality']
              .max()
              .reindex(self.stay_ids)
              .to_numpy(dtype=np.float32)
        )

        self.cxr_store    = cxr_store
        self.report_store = report_store

        n_mortal = (self.stay_mortality > 0).sum()
        logger.info(
            "ICUStayDataset loaded  |  %d stays  |  %d windows  |  "
            "%.1f%% mortal",
            len(self.stay_ids), len(df),
            100.0 * n_mortal / len(self.stay_ids),
        )

    # ── Dataset protocol ─────────────────────────────────────────────────────

    def __len__(self) -> int:
        return len(self.stay_ids)

    def __getitem__(self, idx: int) -> dict:
        stay_id = self.stay_ids[idx]
        windows = self._groups.get_group(stay_id)   # sorted by window_id

        cxr_list,  cxr_meta_list  = [], []
        rep_list,  rep_meta_list  = [], []
        lab_list                  = []
        vital_list                = []
        interv_t_list             = []
        interv_t1_list            = []
        interv_t1_valid_list      = []
        mortality_list            = []

        for _, row in windows.iterrows():
            cxr, cxr_meta     = self._get_cxr(row['cxrs'])
            rep, rep_meta     = self._get_reports(row['reports'])
            lab               = self._get_labs(row['labs'])
            vital             = self._get_vitals(row['vitals'])
            it, it1, it1_v    = self._get_interventions(
                                    row['interventions_t'],
                                    row['interventions_t1'])

            cxr_list.append(cxr);           cxr_meta_list.append(cxr_meta)
            rep_list.append(rep);           rep_meta_list.append(rep_meta)
            lab_list.append(lab)
            vital_list.append(vital)
            interv_t_list.append(it)
            interv_t1_list.append(it1)
            interv_t1_valid_list.append(it1_v)
            mortality_list.append(float(row['mortality']))

        return {
            'stay_id'          : stay_id,
            'cxr'              : cxr_list,            # list[Tensor [n_cxr, D_CXR]]
            'cxr_meta'         : cxr_meta_list,       # list[Tensor [n_cxr, 4]]
            'report'           : rep_list,            # list[Tensor [n_rep, D_REPORT]]
            'report_meta'      : rep_meta_list,       # list[Tensor [n_rep, 4]]
            'lab'              : lab_list,            # list[Tensor [n_lab, 5]]
            'vital'            : vital_list,          # list[Tensor [n_vit, 8]]
            'interventions_t'  : interv_t_list,       # list[Tensor [5]]
            'interventions_t1' : interv_t1_list,      # list[Tensor [5]]
            'interv_t1_valid'  : interv_t1_valid_list,# list[bool]
            'mortality'        : mortality_list,      # list[float]
        }

    # ── per-modality helpers ──────────────────────────────────────────────────

    def _get_cxr(self, cxr_json: dict) -> Tuple[Tensor, Tensor]:
        """
        Returns
        -------
        embeddings : [n_cxrs, D_CXR]   — shape [0, D_CXR] when no CXR
        meta       : [n_cxrs, 4]
                     col 0  mask        = 1.0 (real event)
                     col 1  view_position
                     col 2  t_global    (note: source JSON key is 't_gloabl')
                     col 3  t_window
        """
        events = cxr_json.get('cxrs')
        if not isinstance(events, list):
            return torch.zeros(0, D_CXR), torch.zeros(0, 4)

        embeds, metas = [], []
        for ev in events:
            emb = self.cxr_store.get(str(ev['dicom_id']))
            embeds.append(torch.from_numpy(emb))
            metas.append(torch.tensor(
                [1.0,
                 float(ev['view_position']),
                 float(ev['t_gloabl']),   # preserving source-data typo
                 float(ev['t_window'])],
                dtype=torch.float32,
            ))

        return torch.stack(embeds), torch.stack(metas)

    def _get_reports(self, rep_json: dict) -> Tuple[Tensor, Tensor]:
        """
        Returns
        -------
        embeddings : [n_reports, D_REPORT]
        meta       : [n_reports, 4]
                     col 0  mask      = 1.0
                     col 1  note_type
                     col 2  t_global
                     col 3  t_window
        """
        events = rep_json.get('reports')
        if not isinstance(events, list):
            return torch.zeros(0, D_REPORT), torch.zeros(0, 4)

        embeds, metas = [], []
        for ev in events:
            emb = self.report_store.get(str(ev['note_id']))
            embeds.append(torch.from_numpy(emb))
            metas.append(torch.tensor(
                [1.0,
                 float(ev['note_type']),
                 float(ev['t_global']),
                 float(ev['t_window'])],
                dtype=torch.float32,
            ))

        return torch.stack(embeds), torch.stack(metas)

    def _get_labs(self, lab_json: dict) -> Tensor:
        """
        Returns [n_labs, 5]
            col 0  mask      = 1.0
            col 1  lab_type
            col 2  lab_value
            col 3  t_global
            col 4  t_window
        """
        events = lab_json.get('labs')
        if not isinstance(events, list):
            return torch.zeros(0, 5)

        rows = [
            [1.0,
             float(ev['lab_type']),
             float(ev['lab_value']),
             float(ev['t_global']),
             float(ev['t_window'])]
            for ev in events
        ]
        return torch.tensor(rows, dtype=torch.float32)

    def _get_vitals(self, vital_json: dict) -> Tensor:
        """
        Returns [n_vitals, 8]
            col 0  mask        = 1.0
            col 1  vital_type
            col 2  vital_mean
            col 3  vital_std
            col 4  vital_last
            col 5  vital_count
            col 6  t_global
            col 7  t_window
        """
        events = vital_json.get('vitals')
        if not isinstance(events, list):
            return torch.zeros(0, 8)

        rows = [
            [1.0,
             float(ev['vital_type']),
             float(ev['vital_mean']),
             float(ev['vital_std']),
             float(ev['vital_last']),
             float(ev['vital_count']),
             float(ev['t_global']),
             float(ev['t_window'])]
            for ev in events
        ]
        return torch.tensor(rows, dtype=torch.float32)

    def _get_interventions(
        self,
        it_json  : dict,
        it1_json : dict,
    ) -> Tuple[Tensor, Tensor, bool]:
        """
        Returns
        -------
        it        [5]  current-window intervention labels
        it1       [5]  next-window labels  (zeros for last window)
        it1_valid bool False when this is the last window of the stay.
                       The training loop uses this to skip NWP loss and
                       future-intervention BCE at this position.
        """
        it = torch.tensor(
            [float(it_json[k]) for k in INTERV_KEYS],
            dtype=torch.float32,
        )

        if it1_json.get('last_window', False):
            return it, torch.zeros(5, dtype=torch.float32), False

        it1 = torch.tensor(
            [float(it1_json[k]) for k in INTERV_KEYS],
            dtype=torch.float32,
        )
        return it, it1, True


