"""## Severity Pooling"""

class SeverityPooling(nn.Module):

    def __init__(
        self,
        d_model      : int = 256,
        K            : int = 3,
        severity_dim : int = 64,
        dropout      : float = 0.1,
    ) -> None:
        super().__init__()

        self.K = K

        # Scorer: assigns a scalar weight to each of the K query vectors
        # at each position independently.
        self.scorer = nn.Sequential(
            nn.LayerNorm(d_model),
            nn.Linear(d_model, d_model // 2),
            nn.Tanh(),
            nn.Dropout(dropout),
            nn.Linear(d_model // 2, 1),
        )

        # Projection: pooled d_model vector -> severity_dim
        self.proj = nn.Sequential(
            nn.LayerNorm(d_model),
            nn.Linear(d_model, severity_dim),
            nn.GELU(),
        )

    def forward(
        self,
        severity_queries : torch.Tensor,   # [B, W, K, d_model]
    ) -> torch.Tensor:                     # [B, W, severity_dim]

        # Score each query
        scores  = self.scorer(severity_queries)         # [B, W, K, 1]
        weights = torch.softmax(scores, dim=2)          # [B, W, K, 1]

        # Weighted sum over K queries
        pooled  = (weights * severity_queries).sum(dim=2)  # [B, W, d_model]

        # Project to severity_dim
        return self.proj(pooled)                        # [B, W, severity_dim]


