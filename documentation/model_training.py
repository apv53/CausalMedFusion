        self.net = nn.Sequential(
            nn.LayerNorm(severity_dim),
            nn.Linear(severity_dim, severity_dim),
            nn.GELU(),
            nn.Dropout(dropout),
            nn.Linear(severity_dim, self.N_INTERVENTIONS),
        )

