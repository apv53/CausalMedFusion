@torch.no_grad()
def validate_stage1(model: ICUModel) -> dict:
    """
    Metrics
    -------
    weighted_auroc  : clinically weighted AUROC across 5 intervention vars
    aurocs          : per-variable AUROC (hard threshold at 0.5)
    maes            : per-variable MAE against raw soft labels
    spearman        : per-variable Spearman r against soft labels
    nwp_cosine      : mean cosine similarity of NWP predictions
    val_loss_curr   : mean weighted intervention BCE
    """
    model.eval()

    all_probs   = []
    all_logits  = []
    all_labels  = []
    cosine_sims = []
    bce_vals    = []

    for batch in val_loader:
        cxr         = batch['cxr'].to(device)
        cxr_meta    = batch['cxr_meta'].to(device)
        report      = batch['report'].to(device)
        report_meta = batch['report_meta'].to(device)
        lab         = batch['lab'].to(device)
        vital       = batch['vital'].to(device)
        window_mask = batch['window_mask'].to(device)
        interv_t    = batch['interv_t'].to(device)
        interv_t1_valid = batch['interv_t1_valid'].to(device)

        severity, window_embeddings = model.get_severity(
            cxr, cxr_meta, report, report_meta, lab, vital, window_mask,
        )

        interv_logits = model.intervention_head(severity)          # [B, W, 5]
        interv_probs  = torch.sigmoid(interv_logits)

        valid_mask = window_mask.bool()
        all_logits.append(interv_logits[valid_mask].cpu())
        all_probs.append(interv_probs[valid_mask].cpu())
        all_labels.append(interv_t[valid_mask].cpu())

        # Weighted BCE
        bce = weighted_intervention_bce(interv_logits, interv_t)
        bce_vals.append(
            (bce * window_mask).sum().item() / window_mask.sum().item()
        )

        # NWP cosine similarity
        nwp_valid = window_mask[:, :-1] * interv_t1_valid[:, :-1].float()
        if nwp_valid.sum() > 0:
            nwp_pred   = model.nwp_head(severity[:, :-1, :])
            target_emb = window_embeddings[:, 1:, :]
            cos = F.cosine_similarity(nwp_pred, target_emb, dim=-1)
            cosine_sims.append(
                (cos * nwp_valid).sum().item() / nwp_valid.sum().item()
            )

    all_probs  = torch.cat(all_probs,  dim=0).numpy()    # [N, 5]
    all_logits = torch.cat(all_logits, dim=0).numpy()
    all_labels = torch.cat(all_labels, dim=0).numpy()    # [N, 5]  soft

    aurocs, maes, spearmans = {}, {}, {}

    for i, name in enumerate(INTERV_NAMES):
        probs_i  = all_probs[:, i]
        labels_i = all_labels[:, i]
        hard_i   = (labels_i > 0.5).astype(int)

        # AUROC on hard-thresholded labels (clinical binary interpretation)
        try:
            aurocs[name] = roc_auc_score(hard_i, probs_i)
        except ValueError:
            aurocs[name] = float('nan')

        # MAE against soft labels (measures soft-label calibration)
        maes[name] = mean_absolute_error(labels_i, probs_i)

        # Spearman correlation against soft labels (rank-order quality)
        r, _ = spearmanr(labels_i, probs_i)
        spearmans[name] = float(r) if not np.isnan(r) else 0.0

    # Clinically weighted AUROC (checkpoint criterion)
    w_sum    = sum(AUROC_CLINICAL_W.values())
    w_auroc  = sum(aurocs.get(k, 0) * AUROC_CLINICAL_W[k]
                   for k in AUROC_CLINICAL_W) / w_sum

    model.train()

    return {
        'weighted_auroc' : w_auroc,
        'aurocs'         : aurocs,
        'maes'           : maes,
        'spearmans'      : spearmans,
        'nwp_cosine'     : float(np.mean(cosine_sims)) if cosine_sims else 0.0,
        'val_loss_curr'  : float(np.mean(bce_vals)),
    }


