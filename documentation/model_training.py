"""## EMBEDDING STORE"""

class EmbeddingStore:
    """
    Lazy-loading HDF5 embedding store.

    Layout expected inside the HDF5 file:
        /{group_name}/{id_key}     — 1-D array of string identifiers
        /{group_name}/{embed_key}  — 2-D float array  [n, embed_dim]

    The HDF5 file is opened on first access inside each DataLoader
    worker process to avoid file-handle inheritance bugs when
    num_workers > 0.
    """

    def __init__(
        self,
        hdf5_path  : str,
        group_name : str,
        id_key     : str,
        embed_key  : str,
        embed_dim  : int,
    ) -> None:
        self.hdf5_path  = str(hdf5_path)
        self.group_name = group_name
        self.id_key     = id_key
        self.embed_key  = embed_key
        self.embed_dim  = embed_dim

        # Populated lazily — None until first access
        self._file       = None
        self._id_index  : Optional[Dict[str, int]] = None
        self._embeddings = None

    # ── lazy open ────────────────────────────────────────────────────────────

    def _open(self) -> None:
        if self._file is not None:
            return

        self._file = h5py.File(self.hdf5_path, 'r', swmr=True)
        group      = self._file[self.group_name]

        raw_ids = group[self.id_key][:]
        ids = [
            x.decode('utf-8') if isinstance(x, (bytes, np.bytes_)) else str(x)
            for x in raw_ids
        ]
        self._id_index   = {id_: i for i, id_ in enumerate(ids)}
        self._embeddings = group[self.embed_key]

        logger.info(
            "Opened %s  |  group=%s  |  %d embeddings  |  dim=%d",
            self.hdf5_path, self.group_name,
            len(ids), self._embeddings.shape[1],
        )

    # ── public API ───────────────────────────────────────────────────────────

    def get(self, id_: str) -> np.ndarray:
        """Return embedding for *id_* as float32 ndarray [embed_dim]."""
        self._open()
        idx = self._id_index.get(str(id_))
        if idx is None:
            warnings.warn(
                f"EmbeddingStore({self.group_name}): "
                f"ID {id_!r} not found — returning zero vector.",
                RuntimeWarning, stacklevel=2,
            )
            return np.zeros(self.embed_dim, dtype=np.float32)
        return self._embeddings[idx].astype(np.float32)

    def __len__(self) -> int:
        self._open()
        return len(self._id_index)

    # ── pickle support for DataLoader workers ────────────────────────────────
    # Each worker re-opens the file independently after unpickling.

    def __getstate__(self) -> dict:
        return {k: v for k, v in self.__dict__.items()
                if k not in ('_file', '_id_index', '_embeddings')}

    def __setstate__(self, state: dict) -> None:
        self.__dict__.update(state)
        self._file       = None
        self._id_index   = None
        self._embeddings = None

    def close(self) -> None:
        if self._file is not None:
            self._file.close()
            self._file = self._id_index = self._embeddings = None


