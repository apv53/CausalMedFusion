"""## Window Encoder"""

class WindowEncoder(nn.Module):

    MODALITY_DICT = {'cxr': 0, 'report': 1, 'lab': 2, 'vital': 3}

    def __init__(
        self,
        d_model        : int   = 256,
        d_cxr_embed    : int   = D_CXR,
        d_report_embed : int   = D_REPORT,
        lab_types      : int   = 16,
        vital_types    : int   = 15,
        n_heads        : int   = 8,
        n_layers       : int   = 4,
        dim_ff         : int   = 1024,
        dropout        : float = 0.1,
    ) -> None:
        super().__init__()

        self.d_model = d_model

        # Shared encoders — constructed once, passed into all token builders
        self.global_time_encoder = Time2Vec(d_model)
        self.window_time_encoder = Time2Vec(d_model)
        self.modality_encoder    = nn.Embedding(4, d_model)

        # Token builders
        self.cxr_token_builder = CXRTokenBuilder(
            self.global_time_encoder, self.window_time_encoder,
            self.modality_encoder, self.MODALITY_DICT['cxr'],
            d_model, d_cxr_embed,
        )
        self.report_token_builder = ReportTokenBuilder(
            self.global_time_encoder, self.window_time_encoder,
            self.modality_encoder, self.MODALITY_DICT['report'],
            d_model, d_report_embed,
        )
        self.lab_token_builder = LabTokenBuilder(
            self.global_time_encoder, self.window_time_encoder,
            self.modality_encoder, self.MODALITY_DICT['lab'],
            lab_types, d_model,
        )
        self.vital_token_builder = VitalTokenBuilder(
            self.global_time_encoder, self.window_time_encoder,
            self.modality_encoder, self.MODALITY_DICT['vital'],
            vital_types, d_model,
        )

        # Learnable anchor tokens
        self.modality_summary = nn.Parameter(torch.randn(4, d_model))
        self.cls_token        = nn.Parameter(torch.randn(1, 1, d_model))

        # Pre-norm transformer (norm_first=True more stable than post-norm default)
        self.pre_norm = nn.LayerNorm(d_model)

        encoder_layer = nn.TransformerEncoderLayer(
            d_model         = d_model,
            nhead           = n_heads,
            dim_feedforward = dim_ff,
            dropout         = dropout,
            norm_first      = True,
            batch_first     = True,
        )
        self.transformer = nn.TransformerEncoder(encoder_layer, num_layers=n_layers)

    def forward(
        self,
        cxr         : torch.Tensor,   # [B, MAX_CXRS,    D_CXR]
        cxr_meta    : torch.Tensor,   # [B, MAX_CXRS,    4]
        report      : torch.Tensor,   # [B, MAX_REPORTS, D_REPORT]
        report_meta : torch.Tensor,   # [B, MAX_REPORTS, 4]
        lab         : torch.Tensor,   # [B, MAX_LABS,    5]
        vital       : torch.Tensor,   # [B, MAX_VITALS,  8]
    ) -> torch.Tensor:                # [B, d_model]

        B = cxr.shape[0]

        # Extract event masks (col 0) and forward data (col 1:)
        cxr_mask         = cxr_meta[:, :, 0]          # [B, MAX_CXRS]
        cxr_view_time    = cxr_meta[:, :, 1:]          # [B, MAX_CXRS,    3]

        report_mask      = report_meta[:, :, 0]        # [B, MAX_REPORTS]
        report_type_time = report_meta[:, :, 1:]       # [B, MAX_REPORTS, 3]

        lab_mask         = lab[:, :, 0]                # [B, MAX_LABS]
        lab_data         = lab[:, :, 1:]               # [B, MAX_LABS,    4]

        vital_mask       = vital[:, :, 0]              # [B, MAX_VITALS]
        vital_data       = vital[:, :, 1:]             # [B, MAX_VITALS,  7]

        # Build per-event tokens
        cxr_tokens    = self.cxr_token_builder(cxr, cxr_view_time)
        report_tokens = self.report_token_builder(report, report_type_time)
        lab_tokens    = self.lab_token_builder(lab_data)
        vital_tokens  = self.vital_token_builder(vital_data)

        # Assemble token sequence: [CLS | summaries | vital | lab | report | cxr]
        summary = self.modality_summary.unsqueeze(0).expand(B, -1, -1)  # [B, 4, d_model]
        cls     = self.cls_token.expand(B, -1, -1)                       # [B, 1, d_model]

        tokens = torch.cat(
            [cls, summary, vital_tokens, lab_tokens, report_tokens, cxr_tokens],
            dim=1,
        )
        tokens = self.pre_norm(tokens)

        # Build padding mask
        # Positions 0-4 (CLS + 4 summaries): always False (never masked)
        anchor_mask = torch.zeros(B, 5, dtype=torch.bool, device=tokens.device)

        # Event positions: True where event mask == 0 (padded slot)
        event_mask_float = torch.cat(
            [vital_mask, lab_mask, report_mask, cxr_mask], dim=1
        )                                                                  # [B, total_events]
        event_pad_mask = (event_mask_float == 0).bool()

        padding_mask = torch.cat([anchor_mask, event_pad_mask], dim=1)    # [B, 5+total_events]

        # Run transformer, return CLS output
        out = self.transformer(tokens, src_key_padding_mask=padding_mask)
        return out[:, 0]                                                   # [B, d_model]


