class CXREmbeddingEncoder(nn.Module):
    """
    Projects CXR image embeddings [B, n, D_CXR] -> [B, n, d_model]
    and fuses with view-position embedding.

    view_position: integer label 0-6 (7 classes)
    """

    def __init__(self, d_model: int, d_embed: int) -> None:
        super().__init__()

        self.proj = nn.Sequential(
            nn.Linear(d_embed, 512),
            nn.GELU(),
            nn.Dropout(0.1),
            nn.Linear(512, d_model),
        )
        self.view_embed = nn.Embedding(7, d_model)
        self.fusion     = nn.Linear(d_model * 2, d_model)
        self.norm       = nn.LayerNorm(d_model)

    def forward(
        self,
        embedding     : torch.Tensor,   # [B, n_cxrs, d_embed]
        view_position : torch.Tensor,   # [B, n_cxrs]  long
    ) -> torch.Tensor:                  # [B, n_cxrs, d_model]

        proj_emb = self.proj(embedding)
        view_emb = self.view_embed(view_position.long())

        fused = self.fusion(torch.cat([proj_emb, view_emb], dim=-1))
        return self.norm(fused)


