"""## DATA LOADER"""

def build_dataloader(
    parquet_path      : str,
    cxr_store         : EmbeddingStore,
    report_store      : EmbeddingStore,
    batch_size        : int  = 16,
    num_workers       : int  = 2,
    weighted_sampling : bool = True,
    shuffle           : bool = True,
    drop_last         : bool = False,
) -> Tuple[DataLoader, ICUStayDataset]:
    """
    Build a DataLoader for training or evaluation.

    Parameters
    ----------
    weighted_sampling : bool
        Over-samples mortal stays using inverse class-frequency weights.
        Set True for training, False for validation / test.
    drop_last : bool
        Drop the final incomplete batch — recommended for training to
        keep batch statistics stable.

    Returns
    -------
    loader  : DataLoader
    dataset : ICUStayDataset  (exposed for sampler inspection etc.)
    """
    dataset = ICUStayDataset(parquet_path, cxr_store, report_store)

    sampler = None
    if weighted_sampling:
        is_mortal   = (dataset.stay_mortality > 0).astype(np.float32)
        n_total     = len(is_mortal)
        n_mortal    = float(is_mortal.sum())
        n_nonmortal = float(n_total - n_mortal)

        w_mortal    = n_total / (2.0 * n_mortal)    if n_mortal    > 0 else 1.0
        w_nonmortal = n_total / (2.0 * n_nonmortal) if n_nonmortal > 0 else 1.0
        weights     = np.where(is_mortal, w_mortal, w_nonmortal)

        sampler = WeightedRandomSampler(
            weights     = torch.from_numpy(weights).double(),
            num_samples = len(weights),
            replacement = True,
        )
        shuffle = False   # sampler and shuffle are mutually exclusive
        logger.info(
            "WeightedRandomSampler  |  mortal w=%.3f  |  non-mortal w=%.3f",
            w_mortal, w_nonmortal,
        )

    loader = DataLoader(
        dataset,
        batch_size         = batch_size,
        shuffle            = shuffle if sampler is None else False,
        sampler            = sampler,
        num_workers        = num_workers,
        collate_fn         = collate_fn,
        pin_memory         = True,
        persistent_workers = False,
        drop_last          = drop_last,
    )

    return loader, dataset


