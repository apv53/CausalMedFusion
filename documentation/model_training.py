"""## PAD TENSOR UTILITY"""

def pad_tensor(t: Tensor, max_len: int) -> Tensor:
    """
    Pad (or truncate) dimension-0 of *t* to *max_len*.

        [n, d, ...] → [max_len, d, ...]

    Padding rows are zero-filled.
    """
    n = t.shape[0]
    if n >= max_len:
        return t[:max_len]
    pad_shape = (max_len - n, *t.shape[1:])
    return torch.cat(
        [t, torch.zeros(pad_shape, dtype=t.dtype, device=t.device)], dim=0
    )


