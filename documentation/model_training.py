class CausalBlockCrossAttention(nn.Module):
    """
    Multi-head cross-attention with an explicit additive float mask.

    Entirely manual implementation — no nn.MultiheadAttention — so mask
    application is version-independent and guaranteed correct.

    Parameters
    ----------
    d_model  : total model dimension
    n_heads  : number of attention heads (must divide d_model)
    dropout  : attention weight dropout probability
    """

    def __init__(self, d_model: int, n_heads: int, dropout: float = 0.1) -> None:
        super().__init__()
        assert d_model % n_heads == 0, "d_model must be divisible by n_heads"

        self.d_model  = d_model
        self.n_heads  = n_heads
        self.d_head   = d_model // n_heads
        self.scale    = self.d_head ** -0.5

        self.q_proj   = nn.Linear(d_model, d_model)
        self.k_proj   = nn.Linear(d_model, d_model)
        self.v_proj   = nn.Linear(d_model, d_model)
        self.out_proj = nn.Linear(d_model, d_model)
        self.dropout  = nn.Dropout(dropout)

    def forward(
        self,
        query     : torch.Tensor,   # [B, n_q, d_model]
        key       : torch.Tensor,   # [B, n_k, d_model]
        value     : torch.Tensor,   # [B, n_k, d_model]
        attn_mask : torch.Tensor,   # [B, n_heads, n_q, n_k]  float 0/-inf
    ) -> torch.Tensor:              # [B, n_q, d_model]

        B, n_q, _ = query.shape
        n_k       = key.shape[1]

        # Project and split into heads
        def split_heads(x: torch.Tensor) -> torch.Tensor:
            # [B, seq, d_model] -> [B, n_heads, seq, d_head]
            return x.view(B, -1, self.n_heads, self.d_head).transpose(1, 2)

        Q = split_heads(self.q_proj(query))   # [B, H, n_q, d_head]
        K = split_heads(self.k_proj(key))     # [B, H, n_k, d_head]
        V = split_heads(self.v_proj(value))   # [B, H, n_k, d_head]

        # Scaled dot-product attention
        scores = torch.matmul(Q, K.transpose(-2, -1)) * self.scale
        # scores: [B, H, n_q, n_k]

        # Apply mask — attn_mask is [B, H, n_q, n_k], values 0.0 or -inf
        scores = scores + attn_mask

        # Softmax — -inf positions become 0 after exp
        weights = torch.softmax(scores, dim=-1)     # [B, H, n_q, n_k]
        weights = self.dropout(weights)

        # Weighted sum over values
        out = torch.matmul(weights, V)              # [B, H, n_q, d_head]

        # Merge heads
        out = out.transpose(1, 2).contiguous().view(B, n_q, self.d_model)

        return self.out_proj(out)                   # [B, n_q, d_model]


class SeverityEncoder(nn.Module):
    """
    Produces K severity query vectors for each of the W window positions
    using causal block cross-attention over window embeddings.

    Parameters
    ----------
    d_model     : model dimension (must match WindowEncoder output)
    n_positions : number of window positions (= MAX_WINDOWS = 6)
    K           : number of learned queries per position
    n_heads     : attention heads (must divide d_model)
    dropout     : dropout probability

    Forward inputs
    --------------
    window_embeddings : [B, W, d_model]
    window_mask       : [B, W]  float  1=real window, 0=padded

    Forward output
    --------------
    [B, W, K, d_model]
        output[:, n, :, :] was computed attending only to
        window_embeddings[:, 0:n+1, :].
    """

    def __init__(
        self,
        d_model     : int   = 256,
        n_positions : int   = 6,
        K           : int   = 3,
        n_heads     : int   = 4,
        dropout     : float = 0.1,
    ) -> None:
        super().__init__()

        self.d_model     = d_model
        self.n_positions = n_positions
        self.K           = K
        self.n_queries   = n_positions * K   # 18
        self.n_heads     = n_heads

        # Learned query base vectors [1, n_queries, d_model]
        self.queries_base = nn.Parameter(
            torch.randn(1, self.n_queries, d_model)
        )
        # Per-query positional embedding (encodes position + K-slot identity)
        self.query_pos  = nn.Embedding(self.n_queries, d_model)

        # Positional embedding added to window keys / values
        self.window_pos = nn.Embedding(n_positions, d_model)

        # Manual cross-attention
        self.cross_attn = CausalBlockCrossAttention(d_model, n_heads, dropout)

        # Pre-norm FFN residual block
        self.norm1 = nn.LayerNorm(d_model)
        self.norm2 = nn.LayerNorm(d_model)
        self.ffn   = nn.Sequential(
            nn.Linear(d_model, d_model * 4),
            nn.GELU(),
            nn.Dropout(dropout),
            nn.Linear(d_model * 4, d_model),
        )

    def _build_combined_mask(
        self,
        window_mask : torch.Tensor,   # [B, W]  float  1=real, 0=padded
        W           : int,
    ) -> torch.Tensor:                # [B, n_heads, n_queries, W]  float
        """
        Produces a 4-D float additive mask for CausalBlockCrossAttention.

        Causal component  [1, 1, n_queries, W]:
            queries at position p cannot attend to window keys > p.

        Padding component [B, 1, 1, W]:
            all queries cannot attend to padded window keys.

        Combined: [B, n_heads, n_queries, W] via broadcasting.
        """
        device = window_mask.device
        B      = window_mask.shape[0]

        # ── Causal: [1, 1, n_queries, W] ─────────────────────────────────────
        causal_bool  = build_causal_block_mask(self.n_positions, self.K, device)
        # [n_queries, W] -> [1, 1, n_queries, W]
        causal_float = (
            torch.zeros(self.n_queries, W, dtype=torch.float, device=device)
            .masked_fill_(causal_bool, float('-inf'))
            .unsqueeze(0).unsqueeze(0)
        )

        # ── Padding: [B, 1, 1, W] ────────────────────────────────────────────
        pad_bool  = (window_mask == 0)          # [B, W]
        pad_float = (
            torch.zeros(B, 1, 1, W, dtype=torch.float, device=device)
            .masked_fill_(pad_bool.view(B, 1, 1, W), float('-inf'))
        )

        # ── Combine: [B, n_heads, n_queries, W] via broadcast ────────────────
        combined = (causal_float + pad_float).expand(-1, self.n_heads, -1, -1)

        return combined.contiguous()

    def forward(
        self,
        window_embeddings : torch.Tensor,   # [B, W, d_model]
        window_mask       : torch.Tensor,   # [B, W]  float 1=real, 0=padded
    ) -> torch.Tensor:                      # [B, W, K, d_model]

        B, W, _ = window_embeddings.shape
        device  = window_embeddings.device

        # Add positional encoding to window embeddings (keys / values)
        pos_ids = torch.arange(W, device=device)
        keys    = window_embeddings + self.window_pos(pos_ids).unsqueeze(0)

        # Build queries with positional encoding
        q_ids   = torch.arange(self.n_queries, device=device)
        q_pos   = self.query_pos(q_ids).unsqueeze(0)
        queries = (self.queries_base + q_pos).expand(B, -1, -1).clone()
        # .clone() materialises the expand so in-place residuals don't
        # write back into the shared queries_base parameter buffer

        # Build the 4-D combined mask [B, n_heads, n_queries, W]
        attn_mask = self._build_combined_mask(window_mask, W)

        # Cross-attention (pre-norm on queries)
        attn_out = self.cross_attn(
            query     = self.norm1(queries),
            key       = keys,
            value     = keys,
            attn_mask = attn_mask,
        )
        queries = queries + attn_out

        # FFN residual (pre-norm)
        queries = queries + self.ffn(self.norm2(queries))

        # Reshape [B, n_queries, d] -> [B, W, K, d_model]
        return queries.view(B, self.n_positions, self.K, self.d_model)


