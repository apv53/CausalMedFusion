"""# Architectural Definitions - Window Layer

## Time2Vec
============================================================

Shared time encoder used by all four token builders. Maps a scalar normalised time value to a d_model vector via
one linear + (d_model-1) sinusoidal components.


Input  : [B, n_events]         normalised time values

Output : [B, n_events, d_model]
"""

class Time2Vec(nn.Module):

    def __init__(self, d_model: int) -> None:
        super().__init__()
        self.d_model = d_model

        # Linear component
        self.w0 = nn.Parameter(torch.randn(1))
        self.b0 = nn.Parameter(torch.randn(1))

        # Periodic components
        self.w = nn.Parameter(torch.randn(d_model - 1))
        self.b = nn.Parameter(torch.randn(d_model - 1))

    def forward(self, t: torch.Tensor) -> torch.Tensor:
        # t: [B, n_events] -> [B, n_events, 1]
        t = t.unsqueeze(-1).float()

        v0 = self.w0 * t + self.b0                  # [B, n_events, 1]
        v1 = torch.sin(t * self.w + self.b)         # [B, n_events, d_model-1]

        return torch.cat([v0, v1], dim=-1)           # [B, n_events, d_model]


