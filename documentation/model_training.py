def encode_all_windows(
    encoder     : WindowEncoder,
    cxr         : torch.Tensor,   # [B, W, MAX_CXRS,    D_CXR]
    cxr_meta    : torch.Tensor,   # [B, W, MAX_CXRS,    4]
    report      : torch.Tensor,   # [B, W, MAX_REPORTS, D_REPORT]
    report_meta : torch.Tensor,   # [B, W, MAX_REPORTS, 4]
    lab         : torch.Tensor,   # [B, W, MAX_LABS,    5]
    vital       : torch.Tensor,   # [B, W, MAX_VITALS,  8]
) -> torch.Tensor:                # [B, W, d_model]

    W = cxr.shape[1]
    embeddings = []

    for w in range(W):
        emb = encoder(
            cxr[:, w],
            cxr_meta[:, w],
            report[:, w],
            report_meta[:, w],
            lab[:, w],
            vital[:, w],
        )
        embeddings.append(emb)      # [B, d_model]

    return torch.stack(embeddings, dim=1)   # [B, W, d_model]


