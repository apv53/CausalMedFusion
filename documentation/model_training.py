"""## Stage 1 Loss"""

def weighted_intervention_bce(
    logits  : torch.Tensor,   # [..., 5]
    targets : torch.Tensor,   # [..., 5]  soft labels in [0, 1]
) -> torch.Tensor:            # [...]     scalar per position
    """
    Per-position intervention BCE with:
      - per-variable positive class weighting  (POS_WEIGHTS)
      - per-variable clinical importance weighting (VAR_WEIGHTS)

    BCEWithLogitsLoss manual form with pos_weight:
        loss_i = pos_weight_i * t_i * log(σ(x_i))
               + (1 - t_i) * log(1 - σ(x_i))

    Then weighted mean across variables using VAR_WEIGHTS.
    """
    # Per-variable BCE: [..., 5]
    bce = F.binary_cross_entropy_with_logits(
        logits,
        targets,
        pos_weight  = POS_WEIGHTS,   # broadcast over all leading dims
        reduction   = 'none',
    )

    # Weighted mean over the 5 variables → [...]
    return (bce * VAR_WEIGHTS).sum(dim=-1) / VAR_WEIGHTS.sum()


def stage1_loss(
    model        : ICUModel,
    momentum_enc : MomentumWindowEncoder,
    batch        : dict,
    global_step  : int,
) -> tuple:
    """
    Stage 1 hybrid loss.

    L = alpha_t * L_nwp  +  beta * L_curr_interv  +  gamma * L_next_interv + delta * L_triplet

    L_nwp       : cosine embedding loss  — range [0, 2], scale-invariant
    L_curr/next : weighted intervention BCE per window position

    All losses averaged per valid window per stay, then over the batch.
    """
    cxr             = batch['cxr'].to(device)
    cxr_meta        = batch['cxr_meta'].to(device)
    report          = batch['report'].to(device)
    report_meta     = batch['report_meta'].to(device)
    lab             = batch['lab'].to(device)
    vital           = batch['vital'].to(device)
    window_mask     = batch['window_mask'].to(device)           # [B, W]
    interv_t        = batch['interv_t'].to(device)              # [B, W, 5]
    interv_t1       = batch['interv_t1'].to(device)             # [B, W, 5]
    interv_t1_valid = batch['interv_t1_valid'].to(device)       # [B, W]  bool

    B, W = window_mask.shape

    # ── Online severity representations ──────────────────────────────────────
    severity, window_embeddings = model.get_severity(
        cxr, cxr_meta, report, report_meta, lab, vital, window_mask,
    )
    # severity: [B, W, severity_dim]

    # ── NWP target embeddings from momentum encoder ───────────────────────────
    with torch.no_grad():
        target_embeddings = encode_all_windows(
            momentum_enc.encoder,
            cxr, cxr_meta, report, report_meta, lab, vital,
        )                                                        # [B, W, d_model]
        nwp_targets = target_embeddings[:, 1:, :]               # [B, W-1, d_model]

    # ── Head predictions ──────────────────────────────────────────────────────
    nwp_pred         = model.nwp_head(severity[:, :-1, :])           # [B, W-1, d_model]
    interv_curr_pred = model.intervention_head(severity)              # [B, W,   5]
    interv_next_pred = model.intervention_head(severity[:, :-1, :])  # [B, W-1, 5]

    # ── NWP loss — cosine embedding loss ─────────────────────────────────────
    nwp_valid    = window_mask[:, :-1] * interv_t1_valid[:, :-1].float()  # [B, W-1]
    cos_sim      = F.cosine_similarity(nwp_pred, nwp_targets, dim=-1)     # [B, W-1]
    nwp_cos_loss = (1.0 - cos_sim) * nwp_valid                            # [B, W-1]

    n_nwp_valid  = nwp_valid.sum(dim=1).clamp(min=1)
    nwp_per_stay = nwp_cos_loss.sum(dim=1) / n_nwp_valid                  # [B]

    alpha_t = min(1.0, global_step / max(S1_NWP_WARMUP, 1)) * ALPHA_MAX

    # ── Current intervention loss ─────────────────────────────────────────────
    curr_valid = window_mask                                               # [B, W]

    curr_bce = weighted_intervention_bce(interv_curr_pred, interv_t)      # [B, W]
    curr_bce = curr_bce * curr_valid

    n_curr_valid  = curr_valid.sum(dim=1).clamp(min=1)
    curr_per_stay = curr_bce.sum(dim=1) / n_curr_valid                    # [B]

    # ── Next intervention loss ────────────────────────────────────────────────
    next_valid = nwp_valid                                                 # [B, W-1]

    next_bce = weighted_intervention_bce(
        interv_next_pred, interv_t1[:, :-1, :]
    )                                                                      # [B, W-1]
    next_bce = next_bce * next_valid

    n_next_valid  = next_valid.sum(dim=1).clamp(min=1)
    next_per_stay = next_bce.sum(dim=1) / n_next_valid                    # [B]

    # ── Window Order loss ──────────────────────────────────────────────────────────
    l_order = window_order_loss(model.discriminator, window_embeddings, window_mask)

    # ── Combine ───────────────────────────────────────────────────────────────
    l_nwp  = nwp_per_stay.mean()
    l_curr = curr_per_stay.mean()
    l_next = next_per_stay.mean()


    total = alpha_t * l_nwp + BETA * l_curr + GAMMA * l_next + DELTA * l_order

    loss_dict = {
        'total'  : total.item(),
        'nwp'    : l_nwp.item(),
        'curr'   : l_curr.item(),
        'next'   : l_next.item(),
        'order'  : l_order.item(),
        'alpha_t': alpha_t,
    }
    return total, loss_dict


