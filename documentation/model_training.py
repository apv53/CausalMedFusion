"""## Window Order Discriminator"""

class WindowOrderDiscriminator(nn.Module):
    """
    Given two window embeddings, predicts which comes first.
    Operates on window_embeddings [B, W, d_model] directly,
    before the severity encoder — forces temporal structure
    into the window encoder itself.
    """
    def __init__(self, d_model: int = 256) -> None:
        super().__init__()
        self.net = nn.Sequential(
            nn.Linear(d_model * 2, d_model),
            nn.GELU(),
            nn.Linear(d_model, 1),
        )

    def forward(self, emb_a, emb_b):
        # emb_a, emb_b: [N, d_model]
        # returns logit: positive = a comes before b
        return self.net(torch.cat([emb_a, emb_b], dim=-1)).squeeze(-1)


def window_order_loss(discriminator, window_embeddings, window_mask):
    """
    For random pairs of windows (i, j) where i < j, predict i comes first.
    Label = 1 if a is earlier, 0 if b is earlier.
    Randomly flip pairs 50% of the time for balanced training.
    """
    losses = []
    B = window_embeddings.shape[0]

    for n1 in range(MAX_WINDOWS - 1):
        for n2 in range(n1 + 1, min(n1 + 3, MAX_WINDOWS)):  # only adjacent pairs
            valid = (window_mask[:, n1] * window_mask[:, n2]).bool()
            if valid.sum() == 0:
                continue

            emb1 = window_embeddings[valid, n1, :]   # earlier
            emb2 = window_embeddings[valid, n2, :]   # later

            N = emb1.shape[0]

            # Randomly flip half the pairs
            flip = torch.rand(N, device=emb1.device) > 0.5
            a = torch.where(flip.unsqueeze(-1), emb2, emb1)
            b = torch.where(flip.unsqueeze(-1), emb1, emb2)
            label = (~flip).float()   # 1 if a is earlier, 0 if a is later

            logit = discriminator(a, b)
            losses.append(F.binary_cross_entropy_with_logits(logit, label))

    return torch.stack(losses).mean() if losses else \
           torch.tensor(0.0, device=window_embeddings.device)

"""## ICUModel"""

class ICUModel(nn.Module):
    """
    Container for all model components.

    Overrides train() so that severity_encoder and severity_pooling
    stay in eval mode during Stage 2 regardless of outer mode switches.
    Controlled by self._severity_frozen flag.
    """

    def __init__(
        self,
        d_model        : int = D_MODEL,
        severity_dim   : int = SEVERITY_DIM,
        K              : int = K_QUERIES,
        n_positions    : int = MAX_WINDOWS,
        d_cxr_embed    : int = D_CXR,
        d_report_embed : int = D_REPORT,
        lab_types      : int = 16,
        vital_types    : int = 15,
    ) -> None:
        super().__init__()

        self._severity_frozen = False   # set True at start of Stage 2

        self.window_encoder = WindowEncoder(
            d_model        = d_model,
            d_cxr_embed    = d_cxr_embed,
            d_report_embed = d_report_embed,
            lab_types      = lab_types,
            vital_types    = vital_types,
        )
        self.severity_encoder = SeverityEncoder(
            d_model     = d_model,
            n_positions = n_positions,
            K           = K,
            n_heads     = 4,
        )
        self.severity_pooling = SeverityPooling(
            d_model      = d_model,
            K            = K,
            severity_dim = severity_dim,
        )
        self.nwp_head = NextWindowHead(
            severity_dim = severity_dim,
            d_model      = d_model,
        )
        self.intervention_head = InterventionHead(
            severity_dim = severity_dim,
        )
        self.mortality_head = MortalityHead(
            severity_dim = severity_dim,
        )

        self.discriminator = WindowOrderDiscriminator(d_model=d_model)

    def train(self, mode: bool = True):
        """
        Override so frozen severity modules stay in eval mode even when
        the rest of the model is switched to train mode.
        """
        super().train(mode)
        if self._severity_frozen:
            self.severity_encoder.eval()
            self.severity_pooling.eval()
        return self

    def get_severity(self, cxr, cxr_meta, report, report_meta,
                     lab, vital, window_mask):
        window_embeddings = encode_all_windows(
            self.window_encoder,
            cxr, cxr_meta, report, report_meta, lab, vital,
        )
        severity_queries = self.severity_encoder(window_embeddings, window_mask)
        severity         = self.severity_pooling(severity_queries)
        return severity, window_embeddings

    def forward(self, cxr, cxr_meta, report, report_meta,
                lab, vital, window_mask, inference: bool = False):
        severity, _ = self.get_severity(
            cxr, cxr_meta, report, report_meta, lab, vital, window_mask,
        )
        mortality = self.mortality_head(severity, inference=inference)
        return mortality, severity


