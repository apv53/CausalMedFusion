def train_stage2(model: ICUModel) -> ICUModel:

    print("=" * 60)
    print("STAGE 2 — Mortality head training")
    print(f"Mortality pos_weight: {S2_MORT_POS_WEIGHT:.2f}")
    print("=" * 60)

    # ── Load best Stage 1 checkpoint ─────────────────────────────────────────
    s1_ckpt = CKPT_DIR / "stage1_best.pt"
    if s1_ckpt.exists():
        ckpt = torch.load(s1_ckpt, map_location=device,weights_only=False)
        model.load_state_dict(ckpt['model_state'])
        print(f"Loaded Stage 1 checkpoint  epoch={ckpt['epoch']}  "
              f"w_auroc={ckpt['val_metrics']['weighted_auroc']:.4f}")
    else:
        print("WARNING: No Stage 1 checkpoint — using current weights")

    # ── Freeze severity encoder + pooling ────────────────────────────────────
    for module in (model.severity_encoder, model.severity_pooling):
        for p in module.parameters():
            p.requires_grad_(False)
    model._severity_frozen = True
    print("SeverityEncoder + SeverityPooling frozen")

    # ── Differential LR optimiser ────────────────────────────────────────────
    optimizer = torch.optim.AdamW([
        {'params': list(model.mortality_head.parameters()),
        'lr'    : S2_LR_MORTALITY},
    ], weight_decay=S2_WEIGHT_DECAY)

    effective_batches = len(train_loader) // ACCUMULATION_STEPS
    total_steps       = S2_EPOCHS * effective_batches
    scheduler = torch.optim.lr_scheduler.OneCycleLR(
        optimizer,
        max_lr          = S2_LR_MORTALITY,   # single value matches single param group
        total_steps     = total_steps,
        pct_start       = S2_WARMUP_STEPS / total_steps,
        anneal_strategy = 'cos',
    )

    use_amp = (device.type == 'cuda')
    scaler  = torch.amp.GradScaler('cuda', enabled=use_amp)

    best_auroc        = -1.0
    epochs_no_improve = 0

    for epoch in range(1, S2_EPOCHS + 1):
        model.train()   # train() override keeps severity modules in eval
        epoch_losses = []
        t0           = time.time()

        optimizer.zero_grad()
        accum_count = 0

        pbar = tqdm(train_loader, desc=f"S2 Ep {epoch:02d}", leave=False)
        for batch_idx, batch in enumerate(pbar):

            with torch.amp.autocast('cuda', enabled=use_amp):
                loss, loss_dict = stage2_loss(model, batch)
                loss = loss / ACCUMULATION_STEPS

            if not torch.isfinite(loss):
                accum_count = 0
                optimizer.zero_grad()
                continue

            scaler.scale(loss).backward()
            accum_count += 1

            is_last_batch = (batch_idx == len(train_loader) - 1)
            if accum_count == ACCUMULATION_STEPS or is_last_batch:
                scaler.unscale_(optimizer)
                trainable = (list(model.mortality_head.parameters())
                             + list(model.window_encoder.parameters()))
                nn.utils.clip_grad_norm_(trainable, S2_GRAD_CLIP)
                scaler.step(optimizer)
                scaler.update()
                scheduler.step()
                optimizer.zero_grad()
                accum_count = 0
                epoch_losses.append(loss_dict['total'])
                pbar.set_postfix(loss=f"{loss_dict['total']:.4f}")

        # ── Validation ────────────────────────────────────────────────────────
        val_metrics = validate_stage2(model)
        elapsed     = time.time() - t0

        print(
            f"Ep {epoch:02d}/{S2_EPOCHS}  "
            f"loss={np.mean(epoch_losses):.4f}  "
            f"val_loss={val_metrics['val_loss']:.4f}  "
            f"mean_auroc={val_metrics['mean_auroc']:.4f}  "
            f"t={elapsed:.0f}s"
        )
        auroc_str = "  ".join(
            f"S{n+1}={val_metrics['auroc_by_pos'].get(n, float('nan')):.3f}"
            for n in range(MAX_WINDOWS)
        )
        auprc_str = "  ".join(
            f"S{n+1}={val_metrics['auprc_by_pos'].get(n, float('nan')):.3f}"
            for n in range(MAX_WINDOWS)
        )
        print(f"    AUROC : {auroc_str}")
        print(f"    AUPRC : {auprc_str}")

        # ── Checkpoint + early stopping ───────────────────────────────────────
        if val_metrics['mean_auroc'] > best_auroc:
            best_auroc        = val_metrics['mean_auroc']
            epochs_no_improve = 0
            torch.save({
                'epoch'       : epoch,
                'model_state' : model.state_dict(),
                'optimizer'   : optimizer.state_dict(),
                'val_metrics' : {k: float(v) if not isinstance(v, dict) else
                                {str(kk): float(vv) for kk, vv in v.items()}
                                for k, v in val_metrics.items()},
            }, CKPT_DIR / "stage2_best.pt")
            print(f"    ✓ Saved  (mean_auroc={best_auroc:.4f})")
        else:
            epochs_no_improve += 1
            print(f"    no improve {epochs_no_improve}/{S2_PATIENCE}")
            if epochs_no_improve >= S2_PATIENCE:
                print(f"Early stopping at epoch {epoch}")
                break

    # ── Unfreeze for export ───────────────────────────────────────────────────
    model._severity_frozen = False
    for module in (model.severity_encoder, model.severity_pooling):
        for p in module.parameters():
            p.requires_grad_(True)

    print(f"\nStage 2 complete. Best mean mortality AUROC: {best_auroc:.4f}")
    return model


