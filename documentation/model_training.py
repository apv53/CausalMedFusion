"""## Stage 1 Training Loop"""

def train_stage1(model: ICUModel) -> ICUModel:

    print("=" * 60)
    print("STAGE 1 — Severity representation training")
    print("=" * 60)

    momentum_enc = MomentumWindowEncoder(model.window_encoder, EMA_DECAY)
    momentum_enc.to(device)

    stage1_params = (
        list(model.window_encoder.parameters())
        + list(model.severity_encoder.parameters())
        + list(model.severity_pooling.parameters())
        + list(model.nwp_head.parameters())
        + list(model.intervention_head.parameters())
        + list(model.discriminator.parameters())

    )

    optimizer = torch.optim.AdamW(
        stage1_params, lr=S1_LR, weight_decay=S1_WEIGHT_DECAY,
    )

    # Steps denominator accounts for gradient accumulation
    effective_batches = len(train_loader) // ACCUMULATION_STEPS
    total_steps       = S1_EPOCHS * effective_batches
    scheduler         = torch.optim.lr_scheduler.OneCycleLR(
        optimizer,
        max_lr          = S1_LR,
        total_steps     = total_steps,
        pct_start       = S1_WARMUP_STEPS / total_steps,
        anneal_strategy = 'cos',
    )

    use_amp = (device.type == 'cuda')
    scaler  = torch.amp.GradScaler('cuda', enabled=use_amp)

    best_auroc        = -1.0
    epochs_no_improve = 0
    global_step       = 0   # counts optimizer steps (after accumulation)

    for epoch in range(1, S1_EPOCHS + 1):
        model.train()
        epoch_losses = {'total': [], 'nwp': [], 'curr': [],
                        'next': [], 'order': []}
        nan_steps    = 0
        t0           = time.time()

        optimizer.zero_grad()
        accum_count = 0

        pbar = tqdm(train_loader, desc=f"S1 Ep {epoch:02d}", leave=False)
        for batch_idx, batch in enumerate(pbar):

            with torch.amp.autocast('cuda', enabled=use_amp):
                loss, loss_dict = stage1_loss(
                    model, momentum_enc, batch, global_step,
                )
                # Scale loss by accumulation steps so gradients are
                # averaged rather than summed across micro-batches
                loss = loss / ACCUMULATION_STEPS

            if not torch.isfinite(loss):
                nan_steps += 1
                accum_count = 0
                optimizer.zero_grad()
                pbar.set_postfix(loss="NaN-skip")
                continue

            scaler.scale(loss).backward()
            accum_count += 1

            # Optimizer step every ACCUMULATION_STEPS micro-batches
            # or at the end of the epoch
            is_last_batch = (batch_idx == len(train_loader) - 1)
            if accum_count == ACCUMULATION_STEPS or is_last_batch:
                scaler.unscale_(optimizer)
                grad_norm = nn.utils.clip_grad_norm_(stage1_params, S1_GRAD_CLIP)
                scaler.step(optimizer)
                scaler.update()
                scheduler.step()
                optimizer.zero_grad()

                momentum_enc.update(model.window_encoder)
                global_step += 1
                accum_count  = 0

                for k in epoch_losses:
                    if k in loss_dict:
                        epoch_losses[k].append(loss_dict[k])

                pbar.set_postfix(
                    loss  = f"{loss_dict['total']:.4f}",
                    nwp   = f"{loss_dict['nwp']:.4f}",
                    gnorm = f"{grad_norm:.2f}",
                    alpha = f"{loss_dict['alpha_t']:.2f}",
                    order = f"{loss_dict['order']:.4f}"
                )

        # ── Validation ────────────────────────────────────────────────────────
        val_metrics = validate_stage1(model)
        elapsed     = time.time() - t0
        nan_note    = f"  nan={nan_steps}" if nan_steps else ""

        print(
            f"Ep {epoch:02d}/{S1_EPOCHS}  "
            f"loss={np.mean(epoch_losses['total']):.4f}  "
            f"nwp={np.mean(epoch_losses['nwp']):.4f}  "
            f"curr={np.mean(epoch_losses['curr']):.4f}  "
            f"order={np.mean(epoch_losses['order']):.4f}  "
            f"w_auroc={val_metrics['weighted_auroc']:.4f}  "
            f"nwp_cos={val_metrics['nwp_cosine']:.4f}  "
            f"lr={scheduler.get_last_lr()[0]:.2e}  "
            f"t={elapsed:.0f}s{nan_note}"
        )
        for name in INTERV_NAMES:
            print(
                f"    {name:15s}  "
                f"auroc={val_metrics['aurocs'][name]:.4f}  "
                f"mae={val_metrics['maes'][name]:.4f}  "
                f"spearman={val_metrics['spearmans'][name]:.4f}"
            )

        # ── Checkpoint + early stopping ───────────────────────────────────────
        if val_metrics['weighted_auroc'] > best_auroc:
            best_auroc        = val_metrics['weighted_auroc']
            epochs_no_improve = 0
            torch.save({
                'epoch'       : epoch,
                'model_state' : model.state_dict(),
                'optimizer'   : optimizer.state_dict(),
                'val_metrics' : {k: float(v) if not isinstance(v, dict) else
                                {str(kk): float(vv) for kk, vv in v.items()}
                                for k, v in val_metrics.items()},
                'global_step' : global_step,
            }, CKPT_DIR / "stage1_best.pt")
            print(f"    ✓ Saved  (w_auroc={best_auroc:.4f})")
        else:
            epochs_no_improve += 1
            print(f"    no improve {epochs_no_improve}/{S1_PATIENCE}")
            if epochs_no_improve >= S1_PATIENCE:
                print(f"Early stopping at epoch {epoch}")
                break

    print(f"\nStage 1 complete. Best weighted AUROC: {best_auroc:.4f}")
    return model


